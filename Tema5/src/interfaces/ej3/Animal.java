package interfaces.ej3;

/**
 * Interfaz que define el comportamiento básico de un animal.
 */
public interface Animal {

	/**
	 * Método que indica que el animal está comiendo.
	 */
	void comer();

	/**
	 * Método que indica que el animal está durmiendo.
	 */
	void dormir();

	/**
	 * Método que define el sonido que hace el animal.
	 */
	void hacerRuido();
}
